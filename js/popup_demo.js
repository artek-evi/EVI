var btnAbrirPopup = document.getElementById('enlace-demo-1'),
    contenedor_popup_1 = document.getElementById('contenedor_popup_1'),
    contenido_popup_1 = document.getElementById('contenido_popup_1'),
    btnCerrarPopup = document.getElementById('btn-cerrar-popup');

btnAbrirPopup.addEventListener('click', function () {
    contenedor_popup_1.classList.add('active');
    contenido_popup_1.classList.add('active');
})

btnCerrarPopup.addEventListener('click', function () {
    contenedor_popup_1.classList.remove('active');
    contenido_popup_1.classList.remove('active')
})

